// ÌòÑÏû¨ ÏÑ†ÌÉùÎêú ÌÉ≠ÏùÑ Í¥ÄÎ¶¨ÌïòÎäî Provider
final selectedTabProvider = StateProvider<int>((ref) => 0);

class MainNavigation extends ConsumerStatefulWidget {
  const MainNavigation({super.key});

  @override
  ConsumerState<MainNavigation> createState() => _MainNavigationState();
}

class _MainNavigationState extends ConsumerState<MainNavigation> {
  static bool _hasCheckedVersion = false;
  
  @override
  void initState() {
    super.initState();
    
    // Show version update dialog if needed
    if (!_hasCheckedVersion) {
      _hasCheckedVersion = true;
      WidgetsBinding.instance.addPostFrameCallback((_) {
        _checkForUpdate();
      });
    }
  }
  
  Future<void> _checkForUpdate() async {
    if (globalVersionInfo != null && globalVersionInfo!.needsUpdate) {
      print('üì± Showing update dialog from MainNavigation...');
      try {
        await VersionUpdateDialog.show(context, globalVersionInfo!);
      } catch (e) {
        print('‚ö†Ô∏è Failed to show update dialog: $e');
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    print('üè† MainNavigation: Starting build');
    
    try {
      final selectedTab = ref.watch(selectedTabProvider);
      final unreadCount = ref.watch(totalUnreadCountProvider);
      print('üè† MainNavigation: Selected tab = $selectedTab');
      
      // ÌÉ≠ Î≥ÄÍ≤Ω Ïãú Îç∞Ïù¥ÌÑ∞ Ï¥àÍ∏∞ Î°úÎìú
      ref.listen(selectedTabProvider, (previous, next) {
        print('üì± Tab changed from $previous to $next');
        
        if (next == 1) { // ÏßÄÎèÑ ÌÉ≠ ÏÑ†ÌÉùÏãú
          print('üó∫Ô∏è Map tab selected');
          // ÏßÄÎèÑ ÌÉ≠ÏóêÏÑú Í∂åÌïú ÌôïÏù∏ ÌõÑ ÏúÑÏπòÎ•º Í∞ÄÏ†∏Ïò§ÎèÑÎ°ù Î≥ÄÍ≤Ω
          // MainNavigationÏóêÏÑúÎäî ÏûêÎèôÏúºÎ°ú ÏúÑÏπòÎ•º Í∞ÄÏ†∏Ïò§ÏßÄ ÏïäÏùå
        } else if (next == 3) { // Ï±ÑÌåÖ ÌÉ≠ ÏÑ†ÌÉùÏãú
          ref.read(chatRoomsProvider.notifier).loadChatRooms();
        }
      });

      // Í∞Å ÌÉ≠Ïùò ÌéòÏù¥ÏßÄÎì§ - Ïã§Ï†ú Íµ¨ÌòÑÎêú ÌéòÏù¥ÏßÄÎì§ ÏÇ¨Ïö©
      print('üè† MainNavigation: Creating pages array');
      final pages = [
        const HomePage(),         // Ìôà (Ïã§Ï†ú Íµ¨ÌòÑÎêú Î≤ÑÏ†Ñ)
        const MapPage(),         // ÏßÄÎèÑ (Ïã§Ï†ú Íµ¨ÌòÑÎêú Î≤ÑÏ†Ñ)
        const SparksPage(),      // Ïä§ÌååÌÅ¨ (Ïã§Ï†ú Íµ¨ÌòÑÎêú Î≤ÑÏ†Ñ) 
        const ChatPage(),        // Ï±ÑÌåÖ (Ïã§Ï†ú Íµ¨ÌòÑÎêú Î≤ÑÏ†Ñ)
        const ProfilePage(),     // ÌîÑÎ°úÌïÑ (Ïã§Ï†ú Íµ¨ÌòÑÎêú Î≤ÑÏ†Ñ)
      ];
      
      print('üè† MainNavigation: Pages array created with ${pages.length} pages');
      print('üè† MainNavigation: Creating Scaffold with IndexedStack');

      return Scaffold(
        body: IndexedStack(
          index: selectedTab,
          children: pages,
        ),
        bottomNavigationBar: _CustomBottomNavigationBar(
          currentIndex: selectedTab,
          unreadCount: unreadCount,
          onTap: (index) {
            print('üè† MainNavigation: Tab tapped = $index');
            ref.read(selectedTabProvider.notifier).state = index;
          },
        ),
      );
    } catch (e, stackTrace) {
      print('‚ùå MainNavigation: ERROR in build method');
      print('‚ùå Error: $e');
      print('‚ùå StackTrace: $stackTrace');
      rethrow;
    }
  }
}

class _CustomBottomNavigationBar extends ConsumerWidget {
  final int currentIndex;
  final int unreadCount;
  final Function(int) onTap;

  const _CustomBottomNavigationBar({
    required this.currentIndex,
    required this.unreadCount,
    required this.onTap,
  });

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final timeBasedGradient = ref.watch(timeBasedGradientProvider);
    
    return Container(
      decoration: BoxDecoration(
        gradient: timeBasedGradient,
        boxShadow: [
          BoxShadow(
            color: AppColors.black.withOpacity(0.1),
            blurRadius: 10,
            offset: const Offset(0, -2),
          ),
        ],
      ),
      child: SafeArea(
        child: Padding(
          padding: const EdgeInsets.symmetric(
            horizontal: AppSpacing.sm,
            vertical: AppSpacing.xs,
          ),
          child: Row(
            mainAxisAlignment: MainAxisAlignment.spaceAround,
            children: [
              _buildNavItem(
                context: context,
                index: 0,
                icon: Icons.home_outlined,
                selectedIcon: Icons.home,
                label: 'Ìôà',
              ),
              _buildNavItem(
                context: context,
                index: 1,
                icon: Icons.location_on_outlined,
                selectedIcon: Icons.location_on,
                label: 'Ïä§Ìåü',
              ),
              _buildSparkButton(context),
              _buildNavItem(
                context: context,
                index: 3,
                icon: Icons.chat_bubble_outline,
                selectedIcon: Icons.chat_bubble,
                label: 'Ï±ÑÌåÖ',
                showBadge: unreadCount > 0,
                badgeCount: unreadCount,
              ),
              _buildNavItem(
                context: context,
                index: 4,
                icon: Icons.person_outline,
                selectedIcon: Icons.person,
                label: 'ÌîÑÎ°úÌïÑ',
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildNavItem({
    required BuildContext context,
    required int index,
    required IconData icon,
    required IconData selectedIcon,
    required String label,
    bool showBadge = false,
    int badgeCount = 0,
  }) {
    final isSelected = currentIndex == index;
    
    return GestureDetector(
      onTap: () => onTap(index),
      behavior: HitTestBehavior.opaque,
      child: Container(
        padding: const EdgeInsets.symmetric(
          horizontal: AppSpacing.sm,
          vertical: AppSpacing.xs,
        ),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Stack(
              children: [
                AnimatedContainer(
                  duration: const Duration(milliseconds: 200),
                  padding: const EdgeInsets.all(AppSpacing.xs),
                  decoration: BoxDecoration(
                    color: isSelected
                        ? AppColors.white.withOpacity(0.2)
                        : Colors.transparent,
                    borderRadius: BorderRadius.circular(AppSpacing.borderRadiusSm),
                  ),
                  child: Icon(
                    isSelected ? selectedIcon : icon,
                    size: AppSpacing.iconMd,
                    color: isSelected
                        ? AppColors.sparkActive
                        : AppColors.white.withOpacity(0.7),
                  ),
                ),
                if (showBadge && badgeCount > 0)
                  Positioned(
                    right: 0,
                    top: 0,
                    child: Container(
                      padding: const EdgeInsets.all(4),
                      decoration: const BoxDecoration(
                        color: AppColors.error,
                        shape: BoxShape.circle,
                      ),
                      constraints: const BoxConstraints(
                        minWidth: 16,
                        minHeight: 16,
                      ),
                      child: Text(
                        badgeCount > 99 ? '99+' : badgeCount.toString(),
                        style: AppTextStyles.labelSmall.copyWith(
                          color: AppColors.white,
                          fontSize: 10,
                        ),
                        textAlign: TextAlign.center,
                      ),
                    ),
                  ),
              ],
            ),
            const SizedBox(height: AppSpacing.xs),
            AnimatedDefaultTextStyle(
              duration: const Duration(milliseconds: 200),
              style: AppTextStyles.labelSmall.copyWith(
                color: isSelected
                    ? AppColors.sparkActive
                    : AppColors.white.withOpacity(0.7),
                fontWeight: isSelected
                    ? FontWeight.w600
                    : FontWeight.w400,
              ),
              child: Text(label),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildSparkButton(BuildContext context) {
    final isSelected = currentIndex == 2;
    
    return GestureDetector(
      onTap: () => onTap(2),
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 300),
        width: 60,
        height: 60,
        decoration: BoxDecoration(
          gradient: isSelected
              ? const LinearGradient(
                  begin: Alignment.topLeft,
                  end: Alignment.bottomRight,
                  colors: [
                    AppColors.sparkActive,
                    Color(0xFFFFB000),
                  ],
                )
              : LinearGradient(
                  colors: [
                    AppColors.grey400,
                    AppColors.grey500,
                  ],
                ),
          shape: BoxShape.circle,
          boxShadow: isSelected
              ? [
                  BoxShadow(
                    color: AppColors.sparkActive.withOpacity(0.4),
                    blurRadius: 12,
                    offset: const Offset(0, 4),
                  ),
                ]
              : [
                  BoxShadow(
                    color: AppColors.black.withOpacity(0.1),
                    blurRadius: 4,
                    offset: const Offset(0, 2),
                  ),
                ],
        ),
        child: Stack(
          alignment: Alignment.center,
          children: [
            // ÌéÑÏä§ Ìö®Í≥º (ÏÑ†ÌÉùÎêú ÏÉÅÌÉúÏùº Îïå)
            if (isSelected)
              AnimatedContainer(
                duration: const Duration(seconds: 1),
                width: 70,
                height: 70,
                decoration: BoxDecoration(
                  shape: BoxShape.circle,
                  border: Border.all(
                    color: AppColors.sparkActive.withOpacity(0.3),
                    width: 2,
                  ),
                ),
              ),
            // Î≤àÍ∞ú ÏïÑÏù¥ÏΩò
            isSelected
                ? const SparkIcon(
                    size: 28,
                  )
                : const Icon(
                    Icons.bolt,
                    size: 28,
                    color: AppColors.white,
                  ),
          ],
        ),
      ),
    );
  }
}

